function demo(num1) {
    
   let a = num1.toString();
   let b = a.length;
   let c = Number(a[b-1]) ;

   switch (c) {
    case c = 1 :
        console.log('one');
        
        break;
    case c = 2 :
        console.log('two');
        
        break;
    case c = 3 :
        console.log('three');
        
        break;
    case c = 4 :
        console.log('four');
        
        break;
    case c = 5 :
        console.log('five');
        
        break;
    case c = 6 :
        console.log('six');
        
        break;
    case c = 7 :
        console.log('seven');
        
        break;
    case c = 8 :
        console.log('eight');
        
        break;
    case c = 9 :
        console.log('nine');
        
        break;
   
    default:
        console.log('zero');
        break;
   }

}


    

demo(23219);